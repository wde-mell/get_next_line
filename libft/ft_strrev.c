#include "libft.h"

char		*ft_strrev(char *str)
{
	int		a;
	int		b;
	char	c;

	a = 0;
	b = 0;
	while (str[a] != '\0')
	{
		a++;
	}
	a--;
	while (a > b)
	{
		c = str[b];
		str[b] = str[a];
		str[a] = c;
		a--;
		b++;
	}
	return (str);
}
