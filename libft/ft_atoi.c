#include "libft.h"

int		ft_atoi(const char *str)
{
	int w;
	int s;
	int neg;

	w = 0;
	s = 0;
	neg = 1;
	while ((str[w] >= 8 && str[w] <= 13) || str[w] == 32)
		w++;
	if (str[w] == '-')
		neg = -1;
	if (str[w] == '+' || str[w] == '-')
		w++;
	while (str[w] >= '0' && str[w] <= '9')
	{
		s = (s * 10) + (int)(str[w] - '0');
		w++;
	}
	return (s * neg);
}
