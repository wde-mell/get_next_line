#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	a;

	a = 0;
	while (s[a] != (char)c && s[a] != '\0')
		a++;
	if (s[a] == (char)c)
		return ((char*)&s[a]);
	return (NULL);
}
