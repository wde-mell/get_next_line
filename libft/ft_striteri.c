#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	i = 0;
	if (f != 0 && s != 0)
	{
		while (s[i])
		{
			f(i, &s[i]);
			i++;
		}
	}
}
